#include <minunit/minunit.h>
#include <vcblockchain/byteswap.h>

using namespace std;

TEST_SUITE(test_htonll);

/**
 * Test the happy path for htonll.
 */
TEST(htonll)
{
    TEST_EXPECT(0x0000000000000001LL == htonll(0x0100000000000000LL));
    TEST_EXPECT(0x0000000000000100LL == htonll(0x0001000000000000LL));
    TEST_EXPECT(0x0000000000010000LL == htonll(0x0000010000000000LL));
    TEST_EXPECT(0x0000000001000000LL == htonll(0x0000000100000000LL));
    TEST_EXPECT(0x0000000100000000LL == htonll(0x0000000001000000LL));
    TEST_EXPECT(0x0000010000000000LL == htonll(0x0000000000010000LL));
    TEST_EXPECT(0x0001000000000000LL == htonll(0x0000000000000100LL));
    TEST_EXPECT(0x0100000000000000LL == htonll(0x0000000000000001LL));
}

/**
 * Test the happy path for ntohll.
 */
TEST(ntohll)
{
    TEST_EXPECT(0x0000000000000001LL == ntohll(0x0100000000000000LL));
    TEST_EXPECT(0x0000000000000100LL == ntohll(0x0001000000000000LL));
    TEST_EXPECT(0x0000000000010000LL == ntohll(0x0000010000000000LL));
    TEST_EXPECT(0x0000000001000000LL == ntohll(0x0000000100000000LL));
    TEST_EXPECT(0x0000000100000000LL == ntohll(0x0000000001000000LL));
    TEST_EXPECT(0x0000010000000000LL == ntohll(0x0000000000010000LL));
    TEST_EXPECT(0x0001000000000000LL == ntohll(0x0000000000000100LL));
    TEST_EXPECT(0x0100000000000000LL == ntohll(0x0000000000000001LL));
}
